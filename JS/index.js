document.getElementById('olhinho').addEventListener('mousedown', function() {
    document.getElementById('senha').type = 'text';
  });
  
  document.getElementById('olhinho').addEventListener('mouseup', function() {
    document.getElementById('senha').type = 'password';
  });
  
  document.getElementById('olhinho').addEventListener('mousemove', function() {
    document.getElementById('senha').type = 'password';
  });